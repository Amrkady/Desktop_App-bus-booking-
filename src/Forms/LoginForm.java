    private void logActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logActionPerformed
        // TODO add your handling code here:
        String name=username.getText().trim();
       char[] passChar = password.getPassword();
       String pass = new String(passChar);
       pass = pass.trim();
       
   
       if(name.length() >0 && pass.length() >0){
        Adminhandler uh=new Adminhandler();
            try {
                int valid=  uh.checkUser(name, pass);
                if(valid > -1)
                {
                Userbean ub=new Userbean();
                   ub.setId(valid);
                   ub.setName(name);
                   ub.setPassword(pass);
                  showNextForm(valid);
                  }
                else
                {
                msg.setText("invalid user name and password");
                resetActionPerformed(evt);
                } 
               uh.closeConnection();
            }
            
            catch (SQLException ex) 
            {
                System.out.println("check message "+ex.getMessage());
                 resetActionPerformed(evt);
            }
           
       }
       else
       {
           msg.setText("Enter user and password");
          resetActionPerformed(evt);
       }
    }//GEN-LAST:event_logActionPerformed

