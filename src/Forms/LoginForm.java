 private void showNextForm(int prvId) throws SQLException
    {
        if(prvId ==1) // Admin
        {
           Adminform vu = new Adminform();
            vu.setEnabled(true);
            vu.setVisible(true);
        }
        else  // user
        {
            Usersform vu = new Usersform();
            vu.setEnabled(true);
            vu.setVisible(true);
        }
        this.setEnabled(false);
        this.setVisible(false);
    }
